from sympy import *

print('a')
U = Matrix([
    [1, 2, 3, 4],
    [0, 5, 6, 7],
    [0, 0, 8, 9],
    [0, 0, 0, 10]
])
print(U.det(), 1 * 5 * 8 * 10)

print('b')
L = Matrix([
    [1, 0, 0, 0],
    [3, -2, 0, 0],
    [-11, 8, 5, 0],
    [89, 9, 3, 2]
])
print(L.det(), 1 * -2 * 5 * 2)

D = zeros(5, 5)
D[0, 0] = -2
D[1, 1] = -6
D[2, 2] = 4
D[3, 3] = 3
D[4, 4] = 10
print(D.det(), -2 * -6 * 4 * 3 * 10)
